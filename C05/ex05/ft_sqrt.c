int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb == 2)
		return (0);
	while (i * i < nb)
	{
		if (i > 46342)
			return (0);
		i++;
	}
	if (nb % i == 0 && nb > 0)
		return (i);
	else
		return (0);
}
