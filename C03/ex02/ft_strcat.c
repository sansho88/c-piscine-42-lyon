void	ft_strcat(char *s1, char *s2)
{
	int	size1;
	int	size2;

	size1 = 0;
	size2 = 0;
	while (s1[size1])
	{
		size1++;
	}
	while (s2[size2])
	{
		s1[size1] = s2[size2];
		size1++;
		size2++;
	}
	s1[size1] = '\0';
}
